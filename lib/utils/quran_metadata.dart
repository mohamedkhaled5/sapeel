  /// الحصول على اسم السورة بناءً على رقم الصفحة
  static String getSurahNameByPage(int page) {
    if (page < 1 || page > 604) return "";
    
    int surahIndex = 0;
    for (int i = 0; i < surahStartPages.length; i++) {
      if (page >= surahStartPages[i]) {
        surahIndex = i;
      } else {
        break;
      }
    }
    return surahNames[surahIndex];
  }

  /// الحصول على رقم الجزء بناءً على رقم الصفحة
  static int getJuzByPage(int page) {
